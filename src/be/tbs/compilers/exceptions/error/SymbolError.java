package be.tbs.compilers.exceptions.error;

/**
 * Symbol Error
 * 	Constructed when a certain symbol could not be defined.
 * @author gino
 */
public class SymbolError extends SemanticError {

	/**
	 * Serial Version UID
	 */
	private static final long serialVersionUID = -6953669388280378991L;

	/**
	 * Constructs a Symbol Error with a given reason.
	 * @param message - The reason for this error.
	 */
	public SymbolError(String message) {
		super(message);
	}

	/**
	 * Constructs a Symbol Error with a given reason.
	 * @param message - The reason for this error.
	 * @param cause - The underlying cause for this error.
	 */
	public SymbolError(String message, Throwable cause) {
		super(message, cause);
	}

}
